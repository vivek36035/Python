# 1. Using Curly Braces
d1 = {"name": "Vivek", "age": 21}
print("1. Curly Braces:", d1)

# 2. Using dict() with keyword arguments
d2 = dict(name="Vivek", age=21)
print("2. dict() with keywords:", d2)

# 3. Using list of tuples
d3 = dict([("name", "Vivek"), ("age", 21)])
print("3. List of Tuples:", d3)

# 4. Using list of lists
d4 = dict([["name", "Vivek"], ["age", 21]])
print("4. List of Lists:", d4)
