import numpy as np

arr = np.array([10, 20, 30, 40, 50])

print("1D Array:", arr)

print("First element:", arr[0])
print("Last element:", arr[-1])

print("Sum:", np.sum(arr))
print("Mean:", np.mean(arr))
print("Maximum:", np.max(arr))
print("Minimum:", np.min(arr))

print("Array + 5:", arr + 5)
print("Array * 2:", arr * 2)
